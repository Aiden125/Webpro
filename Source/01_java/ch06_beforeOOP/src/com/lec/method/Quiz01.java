package com.lec.method;

import java.util.Scanner;

//사용자로부터 원하는 단수(2~9) 구구단을 출력하는 프로그램을 구현하시오.
//(단, 단수를 매개변수로 받아 해당 단수의 구구단을 출력하는 부분을 method로 처리한다.
//사용자가 2~9 사이의 수가 아닌 수를 입력할 시 2~9사이의 수를 입력할 때까지 계속 입력 받는다.)
public class Quiz01 {
	public static void main(String[] args) {
		int su;
		Scanner sc = new Scanner(System.in);
		
		do {
			System.out.print("구구단 출력 프로그램입니다. 2~9 사이의 수를 입력하세요 : ");
			su = sc.nextInt();
			
		}while(su<2 && su>9);
		if(su<2 || su>9) {
			System.out.println("잘못된 값입니다. 2~9사이 숫자로 다시 실행해주세요");
		}else {
			gugu(su);
		}
		sc.close();
	}
		
		private static void gugu(int su) { // su가 1초과인 경우 su*facorial(su-1)
			int result = 0;
			for(int i=1;i<=9;i++) {
				result = su*i;
				System.out.println(su+"*"+i+"="+result);
			}
			
		}
			
}
